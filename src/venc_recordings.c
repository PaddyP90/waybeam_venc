/*
 * GET /api/v1/recordings
 *
 * Response:
 * {
 *   "ok": true,
 *   "data": {
 *     "dir": "/mnt/mmcblk0p1",
 *     "free_bytes": 12345678,
 *     "files": [
 *       { "name": "rec_01h00m00s_abcd.ts", "size": 12345678, "mtime": 1713600000 },
 *       ...
 *     ]
 *   }
 * }
 */
static int handle_list(int fd, const HttpRequest *req, void *ctx)
{
    (void)req; (void)ctx;

    /* Collect file entries */
    struct {
        char   name[256];
        long long size;
        long   mtime;
    } entries[512];
    int count = 0;

    DIR *d = opendir(REC_DIR);
    if (!d) {
        const char *err = (errno == ENOENT) ? "SD card not mounted"
                                             : "Cannot open recording directory";
        char buf[256];
        snprintf(buf, sizeof(buf),
            "{\"ok\":false,\"error\":\"%s\"}", err);
        return httpd_send_json(fd, 503, buf);
    }

    struct dirent *de;
    while ((de = readdir(d)) != NULL && count < 512) {
        if (!is_recording(de->d_name)) continue;
        char path[512];
        snprintf(path, sizeof(path), "%s/%s", REC_DIR, de->d_name);
        struct stat st;
        if (stat(path, &st) != 0) continue;
        strncpy(entries[count].name, de->d_name, 255);
        entries[count].name[255] = '\0';
        entries[count].size  = (long long)st.st_size;
        entries[count].mtime = (long)st.st_mtime;
        count++;
    }
    closedir(d);

    /* Free space */
    long long free_bytes = -1;
    struct statvfs svfs;
    if (statvfs(REC_DIR, &svfs) == 0)
        free_bytes = (long long)svfs.f_bavail * (long long)svfs.f_frsize;

    /* Build JSON — heap-allocated to avoid large stack frames */
    /* Estimate: each entry ~128 chars; header ~100 */
    int bufsize = 256 + count * 140;
    char *buf = malloc(bufsize);
    if (!buf) {
        const char *oom = "{\"ok\":false,\"error\":\"out of memory\"}";
        return httpd_send_json(fd, 500, oom);
    }

    int pos = 0;
    pos += snprintf(buf + pos, bufsize - pos,
        "{\"ok\":true,\"data\":{"
        "\"dir\":\"%s\","
        "\"free_bytes\":%lld,"
        "\"files\":[",
        REC_DIR, free_bytes);

    for (int i = 0; i < count && pos < bufsize - 80; i++) {
        /* Escape filename: replace " and \ with safe substitutes */
        char safe[512]; int si = 0;
        for (int j = 0; entries[i].name[j] && si < 500; j++) {
            char c = entries[i].name[j];
            if (c == '"' || c == '\\') safe[si++] = '_';
            else safe[si++] = c;
        }
        safe[si] = '\0';

        pos += snprintf(buf + pos, bufsize - pos,
            "%s{\"name\":\"%s\",\"size\":%lld,\"mtime\":%ld}",
            (i ? "," : ""), safe, entries[i].size, entries[i].mtime);
    }

    pos += snprintf(buf + pos, bufsize - pos, "]}}");
    int rc = httpd_send_json(fd, 200, buf);
    free(buf);
    return rc;
}

/*
 * GET /api/v1/recordings/download?file=filename.ts
 *
 * Streams the file as an octet-stream download.
 * The filename must be a plain name (no path separators).
 */
static int handle_download(int fd, const HttpRequest *req, void *ctx)
{
    (void)ctx;
    char namebuf[256];
    const char *name = query_param(req, "file", namebuf, sizeof(namebuf));
    if (!name || !safe_filename(name)) {
        return httpd_send_json(fd, 400,
            "{\"ok\":false,\"error\":\"missing or invalid file parameter\"}");
    }

    char path[512];
    snprintf(path, sizeof(path), "%s/%s", REC_DIR, name);

    struct stat st;
    if (stat(path, &st) != 0) {
        return httpd_send_json(fd, 404,
            "{\"ok\":false,\"error\":\"file not found\"}");
    }

    int file_fd = open(path, O_RDONLY);
    if (file_fd < 0) {
        return httpd_send_json(fd, 500,
            "{\"ok\":false,\"error\":\"cannot open file\"}");
    }

    /* Determine content-type */
    size_t nlen = strlen(name);
    const char *ct = "application/octet-stream";
    if (nlen > 3 && strcasecmp(name + nlen - 3, ".ts") == 0)
        ct = "video/mp2t";

    /* Emit headers */
    char headers[512];
    snprintf(headers, sizeof(headers),
        "HTTP/1.1 200 OK\r\n"
        "Content-Type: %s\r\n"
        "Content-Length: %lld\r\n"
        "Content-Disposition: attachment; filename=\"%s\"\r\n"
        "Cache-Control: no-cache\r\n"
        "Connection: close\r\n"
        "\r\n",
        ct, (long long)st.st_size, name);

    /* Write headers — use raw write; httpd_send_* functions add their own */
    {
        size_t hlen = strlen(headers);
        size_t written = 0;
        while (written < hlen) {
            ssize_t r = write(fd, headers + written, hlen - written);
            if (r <= 0) { close(file_fd); return -1; }
            written += r;
        }
    }

    /* Stream file */
    char *chunk = malloc(SEND_BUF);
    if (!chunk) { close(file_fd); return -1; }

    ssize_t n;
    while ((n = read(file_fd, chunk, SEND_BUF)) > 0) {
        ssize_t sent = 0;
        while (sent < n) {
            ssize_t r = write(fd, chunk + sent, n - sent);
            if (r <= 0) goto done;
            sent += r;
        }
    }
done:
    free(chunk);
    close(file_fd);
    return 0;
}

