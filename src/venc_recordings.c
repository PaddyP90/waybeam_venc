/* Returns 1 if name ends with .ts or .hevc (case-insensitive) */
static int is_recording(const char *name)
{
    size_t n = strlen(name);
    if (n > 3 && strcasecmp(name + n - 3, ".ts")   == 0) return 1;
    if (n > 5 && strcasecmp(name + n - 5, ".hevc") == 0) return 1;
    return 0;
}

/* Validate a filename: no path separators, no null bytes, non-empty */
static int safe_filename(const char *name)
{
    if (!name || !*name) return 0;
    for (const char *p = name; *p; p++) {
        if (*p == '/' || *p == '\\' || *p == '\0') return 0;
    }
    return 1;
}

/*
 * Parse a single key from req->query ("key=value&key2=value2").
 * Writes the value into `out` (up to `outsz-1` bytes), returns out,
 * or NULL if the key is not present.
 */
static char *query_param(const HttpRequest *req, const char *key,
                         char *out, size_t outsz)
{
    const char *q = req->query;
    size_t klen = strlen(key);
    while (q && *q) {
        /* skip leading '?' or '&' */
        if (*q == '?' || *q == '&') { q++; continue; }
        if (strncmp(q, key, klen) == 0 && q[klen] == '=') {
            const char *v = q + klen + 1;
            size_t i = 0;
            while (*v && *v != '&' && i < outsz - 1)
                out[i++] = *v++;
            out[i] = '\0';
            return out;
        }
        /* advance to next key */
        while (*q && *q != '&') q++;
    }
    return NULL;
}

